# Create a range object: values
values = range(10,21)

# Print the range object
print(values)

# Create a list of integers: values_list
values_list = list(range(10,21))

# Print values_list
print(values_list)

# Get the sum of values: values_sum
values_sum = sum(range(10,21))

# Print values_sum
print(values_sum)
